# What is Joblib? 
# Joblib is a set of tools to provide lightweight pipelining in Python. 
# In particular, joblib offers: transparent disk-caching of the output values and lazy re-evaluation (memoize pattern). 
# This is especially useful when your function is I/O bound, such as reading data from disk, downloading data from the internet, or querying a database.


#Fundamental Data Types in Python
# int and float are the two most common data types in Python.
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))

print(type(9.1+1.1))

print(2 ** 3) #2 to the power of 3
print(5 // 4) #floor division
print(5 % 4) #modulus

# Math Functions
print(round(3.1)) #rounds to the nearest whole number
print(abs(-20)) #absolute value
print(bin(5)) #binary representation of 5
print(int('0b101', 2)) #converts binary to integer